package br.com.curriculos.dao;

public class Dao {
		   private String rg;  
		   private String nome;  
		   private int idade;  
		   private String estado;  
		   private String cidade;  
		  
		   public void setRg(String rg) {  
		      this.rg = rg;  
		   }  
		  
		   public void setNome(String nome) {  
		      this.nome = nome;  
		   }  
		  
		   public void setIdade(int idade) {  
		      this.idade = idade;  
		   }  
		  
		   public void setEstado(String estado) {  
		      this.estado = estado;  
		   }  
		  
		   public void setCidade(String cidade) {  
		      this.cidade = cidade;  
		   }  
		  
		   public String getRg() {  
		      return this.rg;  
		   }  
		  
		   public String getNome() {  
		      return this.nome;  
		   }  
		  
		   public int getIdade() {  
		      return this.idade;  
		   }  
		  
		   public String getEstado() {  
		      return this.estado;  
		   }  
		  
		   public String getCidade() {  
		      return this.cidade;  
		   }  
}
